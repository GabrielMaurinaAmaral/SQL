    Codigo INTEGER NOT NULL PRIMARY KEY,
    NOME VARCHAR(30) NOT NULL,
    SALARIO NUMERIC(12,2),
    GENERO CHAR(1) NOT NULL
        CONSTRAINT GEN_M_F
        CHECK (GENERO IN ('M','F')),
    IDADE INTEGER,
    UF CHAR (2),
    CIDADE VARCHAR (40) NOT NULL,
    EST_CIVIL CHAR (1) NOT NULL
        CHECK (EST_CIVIL IN ('S', 'C', 'D', 'V', 'U')),
    DATA_NASC DATE
);

